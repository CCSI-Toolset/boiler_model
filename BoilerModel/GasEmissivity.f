c  GasEmissivity.f, CALCULATE GAS EMISSIVITY
c
c  FUNCTIONS/SUBROUTINES exported from GasEmissivity.dll:
c      GasEmissivity      - subroutine 
c     emis is the returned emissivity
c  Inputs:
c  TEMP: temperature in K
c  MBL: mean beam length in m
c  PRES:  total presure in Pa
c  FMOLE: mole fraction of CO2, H2O, CH4, CO, O2, N2. should sum up to 1, code needs partial pressures
c  FVSOOT: soot volume fraction
c  Output:
c  EMIS: flame emisivity
c  all variable in double precision
ccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc
      SUBROUTINE GASEMISSIVITY(TEMP, MBL, PRES, FMOLE, FVSOOT, EMIS)
!DEC$ ATTRIBUTES DLLEXPORT::GasEmissivity
      IMPLICIT NONE
      DOUBLE PRECISION TEMP, MBL, PRES, FMOLE(6), FVSOOT, EMIS
      DOUBLE PRECISION W(1),XPART(1),T(1),DD(1)
      DOUBLE PRECISION OMMIN,OMMAX,TWALL,P(6,1),SPECIE(5)
      INTEGER NPT, I, NOM
      DOUBLE PRECISION PTOT
      COMMON/CPART/W,XPART,T,DD,NPT
      COMMON/CMAIN/OMMIN,OMMAX,TWALL,P,SPECIE
      COMMON/CMAIN2/NOM
C
      NPT = 1                      !NUMBER OF ZONE
      DD(1) = MBL                  !MEAN BEAM LENGTH IN M
      T(1) = TEMP                  !FLAME TEMPERATURE IN K
C  CONVERT PRESSURE FROM PA TO ATM
      PTOT = PRES/101325.
C  CALCULATE SPECIES PARTIAL PRESSURE IN ATM FROM MOLE FRACTION FMOLE
      DO I=1,6
      P(I,1) = FMOLE(I)*PTOT
      ENDDO
      W(1) = FVSOOT            !SOOT VOLUME FRACTOIN
      SPECIE(1)=P(1,1)
      SPECIE(2)=P(2,1)
      SPECIE(3)=P(3,1)
      SPECIE(4)=P(4,1)
      SPECIE(5)=W(1)
      TWALL = 0.0         !WALL TEMPERATURE, SET TO VERY SMALL NUMBER
      OMMIN = 50.         !MINIMUM WAVE NUMBER IN CM-1
      OMMAX = 10000.      !MAXIMUM WAVE NUMBER IN CM-1
      NOM=INT((1100.-OMMIN)/5.)+INT((5000.-1100.)/25.)
     2        +INT((OMMAX-5000.)/50.)
      CALL RADCAL(EMIS)
      RETURN
      END
