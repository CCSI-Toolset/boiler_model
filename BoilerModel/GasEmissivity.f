      SUBROUTINE RADCAL(EMIS)
      IMPLICIT NONE
      DOUBLE PRECISION SDWEAK,GDINV,GDDINV,XC,AOM,Q,QW(500),EMIS,
     2        TTAU(500),XTOT(1),XT(500),XSTAR(1),X(4,1),UK,TAU(1)
      DOUBLE PRECISION OMMIN,OMMAX,TWALL,P(6,1),SPECIE(5)
      DOUBLE PRECISION GAMMA(4,7)
      DOUBLE PRECISION W(1),XPART(1),T(1),DD(1)
      INTEGER NPT,NOM,I,J,II,KK,KMIN,KMAX,MM,N,NM
      COMMON/CMAIN/OMMIN,OMMAX,TWALL,P,SPECIE
      COMMON/CMAIN2/NOM
      COMMON/CPARAM/GAMMA
      COMMON/CPART/W,XPART,T,DD,NPT
      DOUBLE PRECISION U(4,1),AC(1),AD(1),GC(4,1),AMBDA(500),TAUS(1),
     2          AB(500)
      DOUBLE PRECISION ABGAS,ABIL,ABIS,ABLONG,ABSHRT,ARG,ARGNEW
      DOUBLE PRECISION DAMBDA,DOM,ENN,GKD,GKDD,OMEGA,PTOT,RSL,RSS
      DOUBLE PRECISION Y,YC,YD,TEMP,WL,XD,XX,PLANCK
C
C  [NOTE: THE TOTAL INTENSITY CALCULATED IS THAT WHICH LEAVES INTERVAL J=1.
C  P(I,J) IS PARTIAL PRESSURE, ATM, OF SPECIES I IN  INTERVAL J.
C  I=1,2,3,4,5, OR 6 IMPLIES SPECIES IS CO2, H2O, CH4, CO, O2, OR N2, RESP.]
C
      DOM=5.0
      OMEGA=OMMIN-DOM
      NM=NOM-1
C
C     LOOP 1000 COMPUTES EACH SPECTRAL CONTRIBUTION
C     *********************************************
      DO 1000 KK=1,NOM
      OMEGA=OMEGA+DOM
      IF(OMEGA.LE.1100.)GO TO 109
      OMEGA=OMEGA+20.
      IF(OMEGA.LE.5000.)GO TO 109
      OMEGA=OMEGA+25.
109   AMBDA(KK)=10000./OMEGA
      ABGAS=0.
C
C          LOOP 200 COMPUTES THE CONTRIBUTION OF EACH SPECIES TO TAU
C          *********************************************************
      DO 200 I=1,4
C     IF SPECIE(I) IS SET TO 0., THAT PARTICULAR RADIATING SPECIES IS
C     NOT PRESENT.  THE SPECIES CONSIDERED ARE
C          I   SPECIES
C          1     CO2
C          2     H2O
C          3     CH4
C          4     CO
C          5     PARTICULATES
      IF(SPECIE(I).EQ.0.) GO TO 200
C
C               LOOP 100 IS FOR EACH ELEMENT ALONG PATH
C               ***************************************
      DO 100 J=1,NPT
C     (CALCULATION PROCEEDS IN ACCORDANCE WITH THE SLG MODEL, TABLE 5-18
C     IN NASA SP-3080.)
      IF(KK.GT.1) GO TO 107
      U(I,J)=273./T(J)*P(I,J)*100.*DD(J)
      GC(I,J)=0.
      PTOT=0.
      DO 105 II=1,6
      PTOT=P(II,J)+PTOT
105   GC(I,J)=GC(I,J)+GAMMA(I,II)*P(II,J)*(273./T(J))**.5
      GC(I,J)=GC(I,J)+GAMMA(I,7)*P(I,J)*273./T(J)
107   IF(P(I,J).EQ.0.) GO TO 121
      TEMP=T(J)
      GO TO(101,102,103,104),I
101   CALL CO2(OMEGA,TEMP,GC(1,J),SDWEAK,GDINV,GDDINV)
      GO TO 108
102   CALL H2O(OMEGA,TEMP,GC(2,J),SDWEAK,GDINV,GDDINV)
      GO TO 108
103   CONTINUE
      CALL FUEL(OMEGA,TEMP,P(3,J),PTOT,GC(3,J),SDWEAK,GDINV,GDDINV)
      GO TO 108
104   CONTINUE
      CALL CO(OMEGA,TEMP,GC(4,J),SDWEAK,GDINV,GDDINV)
108   UK=SDWEAK*U(I,J)
      IF(J.EQ.1) GO TO 110
      GKD=UK*GDINV
      GKDD=UK*GDDINV
      XSTAR(J)=XSTAR(J-1)+UK
      AD(J)=(XSTAR(J-1)*AD(J-1)+GKDD)/XSTAR(J)
      AC(J)=(XSTAR(J-1)*AC(J-1)+GKD)/XSTAR(J)
      GO TO 115
110   XSTAR(1)=UK+1.D-34
      ABGAS=UK/DD(1)+ABGAS
      AD(1)=GDDINV
      AC(1)=GDINV
115   IF(XSTAR(J).LT.1.E-6) GO TO 125
      XD=1.7*AD(J)*(DLOG(1.+(XSTAR(J)/1.7/AD(J))**2.))**.5
      YD=1.-(XD/XSTAR(J))**2.
      XC=XSTAR(J)/(1.+XSTAR(J)/4./AC(J))**.5
C
C   THE FOLLOWING LOOP COMPUTES THE OPTICAL THICKNESS, XC, FOR METHANE USING
C   THE GODSON EQUATION AND AN APPROXIMATION TO THE LADENBERG-REICHE
C   FUNCTION AS RECOMMENDED BY BROSMER AND TIEN (JQSRT 33,P 521).  THE
C   ERROR FUNCTION IS FOUND FROM ITS SERIES EXPANSION.
C
      IF(I.NE.3.) GO TO 118
      IF(XC.GT.10.)GO TO 118
      AOM=XC
      XX=.5*3.141593**.5*XC
      IF(XX.LE.3.)GO TO 111
      AOM=1.-EXP(-XX**2.)/(3.141593**.5*XX)
      GO TO 117
111   ENN=1.
      DO 116 N=1,30
      ENN=ENN*N
      MM=2*N+1
      ARG=1.128379*(-1.)**N*((.88622693*XC)**MM)/(MM*ENN)
      ARGNEW=ARG+AOM
C     IF(ABS(ARG/ARGNEW).LT..000001)N=30
116   AOM=ARGNEW
117   IF(AOM.GE.1.)AOM=.9999999
      XC=-DLOG(1.-AOM)
C
C
118   YC=1.-(XC/XSTAR(J))**2.
      Y=1./YC**2.+1./YD**2.-1.
C Modified by Jinliang Ma, avoid math error
      if (y.lt.1.) y = 1.
      X(I,J)=XSTAR(J)*((1.-(Y**(-.5)))**.5)
      GO TO 100
121   IF(J.GT.1) GO TO 123
      XSTAR(1)=1.D-34
      AC(1)=1.
      AD(1)=1.
      GO TO 125
123   XSTAR(J)=XSTAR(J-1)
      AC(J)=AC(J-1)
      AD(J)=AD(J-1)
125   X(I,J)=XSTAR(J)
100   CONTINUE
C
C
200   CONTINUE
C
C  DETERMINE OPTICAL DEPTH OF SOOT
C
      IF(SPECIE(5).EQ.0.) GO TO 250
      CALL POD(OMEGA)
      GO TO 260
250   DO 255 J=1,NPT
255   XPART(J)=0.
260   CONTINUE
      AB(KK)=ABGAS+XPART(1)/DD(1)
C
C     EVALUATE THE COMBINED SPECTRAL TRANSMITTANCE AND RADIANCE
C     *********************************************************
      DO 500 J=1,NPT
      XTOT(J)=0.
      DO 300 I=1,4
      IF(SPECIE(I).EQ.0.) X(I,J)=0.
      XTOT(J)=X(I,J)+XTOT(J)
300   continue
      XTOT(J)=XTOT(J)+XPART(J)
      IF(XTOT(J).GE.99.) GO TO 305
      TAU(J)=DEXP(-XTOT(J))
      GO TO 310
305   TAU(J)=0.
310   IF(J.EQ.1) GO TO 510
      CALL PLANCK_FUNC(T(J),AMBDA(KK),PLANCK)
      QW(KK)=QW(KK)-(TAU(J)-TAU(J-1))*PLANCK
      GO TO 500
510   CALL PLANCK_FUNC(T(1),AMBDA(KK),PLANCK)
      QW(KK)=-(TAU(1)-1.)*PLANCK
500   CONTINUE
      XT(KK)=XTOT(NPT)
      TTAU(KK)=TAU(NPT)
      CALL PLANCK_FUNC(TWALL,AMBDA(KK),PLANCK)
      QW(KK)=QW(KK)+TTAU(KK)*PLANCK
1000  CONTINUE
C
C  INTEGRATE THE RADIANCE OVER THE SPECTRUM
C
      Q=QW(1)*(AMBDA(1)-AMBDA(2))
      DO 1100 KK=2,NM
1100  Q=Q+QW(KK)*(AMBDA(KK-1)-AMBDA(KK+1))/2.
      Q=Q+QW(NOM)*(AMBDA(NOM-1)-AMBDA(NOM))
C
C  DETERMINE SOOT RADIANCE FOR SHORT AND LONG WAVELENGTHS.
C
      RSL=0.
      RSS=0.
      ABLONG=0.
      ABSHRT=0.
      ABIL=0.
      ABIS=0.
      IF(SPECIE(5).EQ.0..AND.TWALL.EQ.0.)GO TO 1090
      KMAX=OMMIN/5*5
      DO 1040 KK=5,KMAX,5
      OMEGA=FLOAT(KK)
      WL=10000./OMEGA
      DAMBDA=10000./(OMEGA-2.5)-10000./(OMEGA+2.5)
      CALL POD(OMEGA)
      DO 1020 J=1,NPT
      IF(XPART(J).GE.33.) GO TO 1010
      TAUS(J)=EXP(-XPART(J))
      GO TO 1012
1010  TAUS(J)=0.
1012  IF(J.EQ.1)GO TO 1021
      CALL PLANCK_FUNC(T(J),WL,PLANCK)
      RSL=RSL-(TAUS(J)-TAUS(J-1))*PLANCK*DAMBDA
      GO TO 1020
1021  CALL PLANCK_FUNC(T(1),WL,PLANCK)
      RSL=RSL-(TAUS(1)-1.)*PLANCK*DAMBDA
      CALL PLANCK_FUNC(T(1),WL,PLANCK)
      ABLONG=ABLONG+XPART(1)/DD(1)*PLANCK*DAMBDA*5.5411E7
     2       /(T(1))**4.
      CALL PLANCK_FUNC(TWALL,WL,PLANCK)
      ABIL=ABIL+XPART(1)/DD(1)*PLANCK*DAMBDA*5.5411E7
     2     /(TWALL+.000001)**4.
1020  CONTINUE
      CALL PLANCK_FUNC(TWALL,WL,PLANCK)
      RSL=RSL+TAUS(NPT)*PLANCK*DAMBDA
1040  CONTINUE
      KMIN=OMMAX/100*100
      DO 1080 KK=KMIN,25000,100
      OMEGA=FLOAT(KK)
      WL=10000./OMEGA
      DAMBDA=10000./(OMEGA-50.)-10000./(OMEGA+50.)
      CALL POD(OMEGA)
      DO 1070 J=1,NPT
      IF(XPART(J).GE.33.) GO TO 1050
      TAUS(J)=EXP(-XPART(J))
      GO TO 1060
1050  TAUS(J)=0.
1060  IF(J.EQ.1)GO TO 1071
      CALL PLANCK_FUNC(T(J),WL,PLANCK)
      RSS=RSS-(TAUS(J)-TAUS(J-1))*PLANCK*DAMBDA
      GO TO 1070
1071  CALL PLANCK_FUNC(T(1),WL,PLANCK)
      RSS=RSS-(TAUS(1)-1.)*PLANCK*DAMBDA
      CALL PLANCK_FUNC(T(1),WL,PLANCK)
      ABSHRT=ABSHRT+XPART(1)/DD(1)*PLANCK*DAMBDA*5.5411E7
     2       /(T(1))**4.
      CALL PLANCK_FUNC(TWALL,WL,PLANCK)
      ABIS=ABIS+XPART(1)/DD(1)*PLANCK*DAMBDA*5.5411E7
     2     /(TWALL+.000001)**4.
1070  CONTINUE
      CALL PLANCK_FUNC(TWALL,WL,PLANCK)
      RSS=RSS+TAUS(NPT)*PLANCK*DAMBDA
1080  CONTINUE
1090  CONTINUE
      Q=Q+RSS+RSL
C Finally Calculate emissivity (pi*Q/sigma/T^4)
      EMIS = 5.5407E7*Q/T(1)**4.
      IF (EMIS.GT.0.9999) EMIS=0.9999
      RETURN
      END
